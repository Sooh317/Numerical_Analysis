import numpy as np

for n in range(1,10):
    Tn = np.zeros((n, n))
    for i in range(n):
        Tn[i][i] = 1 / (2*(i+1) + 1)
        if i + 1 < n:
            Tn[i][i + 1] = 1
            Tn[i + 1][i] = 1
    ansn, ansv = np.linalg.eig(Tn)
    print(ansn)

